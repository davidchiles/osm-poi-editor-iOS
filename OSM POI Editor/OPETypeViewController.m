- (void)viewDidLoad
{
    [super viewDidLoad];
    
    typeArray = [[category.types allValues] sortedArrayUsingComparator:^NSComparisonResult(id a, id b) {
        NSString *first = [(OPEType*)a displayName];
        NSString *second = [(OPEType*)b displayName];
        return [first compare:second];
